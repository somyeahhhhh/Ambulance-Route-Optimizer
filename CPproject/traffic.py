DEFAULT_PATTERN = [10,8,8,8,10,15,30,55,62,50,40,38, 42,40,38,42,50,62,70,60,48,36,25,14]


def get_congestion_score(node_id, hour, day_type="weekday"):
    """Return congestion score (0–100) for a node at a given hour."""
    pattern = ZONE_PATTERNS.get(node_id, DEFAULT_PATTERN)
    score = pattern[hour % 24]
    if day_type == "weekend":
        # Weekends: less morning peak, more midday
        score = int(score * 0.75 + 15)
        score = min(score, 95)
    return score


def get_traffic_multiplier(node_id, hour, day_type="weekday"):
    """
    Returns a speed multiplier.
    Score=0  → multiplier=1.0  (full speed)
    Score=100 → multiplier=3.5  (very slow, ~28% of normal speed)
    """
    score = get_congestion_score(node_id, hour, day_type)
    return 1.0 + (score / 100) * 2.5


def congestion_label(score):
    if score < 25:
        return "LOW"
    elif score < 50:
        return "MODERATE"
    elif score < 75:
        return "HIGH"
    else:
        return "SEVERE"


def time_label(hour):
    period = "AM" if hour < 12 else "PM"
    h = hour if hour <= 12 else hour - 12
    h = 12 if h == 0 else h
    return f"{h}:00 {period}"
